public class Main {
    public static void main(String[] args) {
        int x=9;
        int y=9;

        int xInc = x+1;
        System.out.println("The incremented value of x is: " + xInc);
        int yInc = y+1;
        System.out.println("The incremented value of y is: " + yInc);

        int resultingMult = xInc * yInc;

        System.out.println("The result of the multiplication of the incremented values is: " + resultingMult);
    }
}